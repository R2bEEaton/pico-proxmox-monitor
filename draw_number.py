def draw_number(h75, num, x, y, r, g, b):
    num = str(num)
    for i in range(len(num)):
        c = num[i]
        if c in "0123456789.":
            w = draw_funcs["0123456789.".index(c)](h75, x, y, r, g, b)
            x += w + 1
            
